const languages = [
  { code: 'es', label: 'Español', Flag: FlagES },
  { code: 'en', label: 'English', Flag: FlagUS },
  { code: 'pt-BR', label: 'Português', Flag: FlagBR },
];

export function LanguageSelector() {
  const locale = useLocale();
  const router = useRouter();
  const pathname = usePathname();

  const currentLanguage = languages.find((lang) => lang.code === locale) || languages[0];

  const handleLanguageChange = (newLocale: string) => {
    // Get pathname without locale (e.g., /es/landing -> /landing)
    const segments = pathname.split('/').filter(Boolean);
    // Remove the locale segment (first segment)
    const pathWithoutLocale = segments.length > 1 
      ? '/' + segments.slice(1).join('/')
      : '/landing'; // Default to landing page if no path
    
    // Navigate to new locale
    router.push(`/${newLocale}${pathWithoutLocale}`);
    router.refresh(); // Refresh to load new locale messages
  };

  const CurrentFlag = currentLanguage.Flag;

  return (
    <DropdownMenu>
      <DropdownMenuTrigger asChild>
        <Button variant="ghost" className="language-selector__trigger">
          <span className="language-selector__flag">
            <CurrentFlag />
          </span>
          <span className="language-selector__label">{currentLanguage.label}</span>
          <Icon path={mdiChevronDown} size={0.8} />
        </Button>
      </DropdownMenuTrigger>
      <DropdownMenuContent align="end" className="language-selector__content">
        {languages.map((lang) => {
          const FlagComponent = lang.Flag;
          return (
            <DropdownMenuItem
              key={lang.code}
              onClick={() => handleLanguageChange(lang.code)}
              className={`language-selector__item ${
                locale === lang.code ? 'language-selector__item--active' : ''
              }`}
            >
              <span className="language-selector__item-flag">
                <FlagComponent />
              </span>
              <span className="language-selector__item-label">{lang.label}</span>
            </DropdownMenuItem>
          );
        })}
      </DropdownMenuContent>
    </DropdownMenu>
  );
}

